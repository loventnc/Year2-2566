public class DisplayDefaultValue {
    static short Default_short;
    static int Default_int;
    static long Default_long;
    static float Default_float;
    static double Default_double;
    static char Default_char;
    static String Default_String;
    static boolean Default_boolean;
    public static void main(String[] args) {
        System.out.println("Default value of Short is: " + Default_short);
        System.out.println("Default value of Integer is: " + Default_int);
        System.out.println("Default value of Long is: " + Default_long);
        System.out.println("Default value of Float is: " + Default_float);
        System.out.println("Default value of Double is: " + Default_double);
        System.out.println("Default value of Character is: " + Default_char);
        System.out.println("Default value of String is: " + Default_String);
        System.out.println("Default value of Boolean is: " + Default_boolean);
    }
}
